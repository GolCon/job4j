package ru.job4j.profession;

public class Profession {
    String name;
    String surname;
    String education;
    long birthday;

    public String getName() {
        return this.name;
    }

    public String getSurname() {
        return this.surname;
    }

    public String getEducation() {
        return this.education;
    }

    public long getBirthday() {
        return this.birthday;
    }

}
